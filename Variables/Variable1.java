class Variable1 
{
	public static void main(String[] args) 
	{
		//variable declaration 
		String name;
		byte age;
		char grade;
		float salary;
		long phno;
		
		//variable initialization
		name = "Khilesh";
		age = 21;
		grade = 'z';
		salary = 5000.00f;
		phno = 9852654285l;
		
		//Printing Variable
		System.out.println(name);
		System.out.println(age);
		System.out.println(grade);
		System.out.println(salary);
		System.out.println(phno);
	}
}
